// length 
// push()  --- new length
// unshift() --- new length
// pop()   ---- lastElement
// shift()  ---- firstName
// includes()  --- boolean value 

// 1.push
let q = ['pune','mumbai','nagpur']
q.push('banglore')
console.log(q)

// 2.unshift
q.unshift('shimla')
console.log(q)

// 3.pop
q.pop()
console.log(q)

//4. shift
q.shift()
console.log(q)

//5. includes
let w = q.includes("mumbai")
console.log(w)


// 6. length property
console.log(q.length)